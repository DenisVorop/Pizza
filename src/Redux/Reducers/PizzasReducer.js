const SET_PIZZAS = 'SET_PIZZAS';

//========================================================================================================================================================

const initialState = {
    items: [],
    isLoaded: false,
}

//=============REDUCER===========================================================================================================================================

const pizzasReducer = (state = initialState, action) => {
    switch (action.type) {
        case SET_PIZZAS: {
            return {
                ...state,
                items: action.payload,
                isLoaded: true
            }
        }
        default: {
            return state;
        }
    }
}

//============ACTION CREATOR============================================================================================================================================

export const setPizzas = (items) => {
    return {
        type: SET_PIZZAS,
        payload: items,
    }
}


//========================================================================================================================================================

export default pizzasReducer;
